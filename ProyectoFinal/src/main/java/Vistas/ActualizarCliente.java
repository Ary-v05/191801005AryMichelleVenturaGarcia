    private void jButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActualizarActionPerformed
        // TODO add your handling code here:
        String nombre = jTextField3.getText();
        String numero = jTextField4.getText();      
        
        int id = Integer.parseInt(jLabelID.getText());
        Clientes cliente = new Clientes( id,nombre, numero);
      
        try{
            DaoClientes.update(cliente);
        } catch (SQLException ex){
            Logger.getLogger(ActualizarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            jTextField3.setText("");
            jTextField4.setText("");
        
            try{
                this.crearModelo();
            } catch (SQLException ex){
                Logger.getLogger(ActualizarCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        jList1.setModel(modelo);
        }
     
    }//GEN-LAST:event_jButtonActualizarActionPerformed

