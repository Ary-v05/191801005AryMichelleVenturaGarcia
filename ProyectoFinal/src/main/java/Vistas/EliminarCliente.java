    private void jListEliminarEMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListEliminarEMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 2){
            int index = jListEliminarE.getSelectedIndex();           
            Clientes clientes = (Clientes)modelo.getElementAt(index);
            try{                
                DaoClientes.delete(clientes);
            } catch (SQLException ex){
                Logger.getLogger(EliminarCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
            finally{
                
                try{
                    this.crearModelo();
                } catch (SQLException ex){
                    Logger.getLogger(EliminarCliente.class.getName()).log(Level.SEVERE, null, ex);
                }
                jListEliminarE.setModel(modelo);
            }
        }
    }//GEN-LAST:event_jListEliminarEMouseClicked

